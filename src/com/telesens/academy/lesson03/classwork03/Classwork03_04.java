package com.academy.telesens.lesson03.inclass03;



public class InClass04 {
    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
            if (i % 2 == 1) {
                System.out.println(i);
            }
        }

        int i = 1;
        while (i < 10) {
            if (i % 2 == 1) {
                System.out.println(i);

            }
            i++;
        }


    }
}
